export type PatternCategory =
  | "spaceships"
  | "oscillators"
  | "still-lifes"
  | "seeds"
  | "guns";

export type LifePattern = {
  id: string;
  name: string;
  description: string;
  category: PatternCategory;
  cells: readonly [number, number][];
};

const fromRows = (
  category: PatternCategory,
  id: string,
  name: string,
  description: string,
  rows: readonly string[],
): LifePattern => ({
  id,
  name,
  description,
  category,
  cells: rows.flatMap((row, y) =>
    [...row].flatMap((cell, x) =>
      cell === "O" ? [[x, y] as [number, number]] : [],
    ),
  ),
});

