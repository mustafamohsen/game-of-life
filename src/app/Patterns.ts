export type LifePattern = {
  id: string;
  name: string;
  description: string;
  cells: readonly [number, number][];
};

const fromRows = (
  id: string,
  name: string,
  description: string,
  rows: readonly string[],
): LifePattern => ({
  id,
  name,
  description,
  cells: rows.flatMap((row, y) =>
    [...row].flatMap((cell, x) => (cell === "O" ? [[x, y] as [number, number]] : [])),
  ),
});

